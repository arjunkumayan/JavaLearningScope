package SuperKeyword;

public class ParentClass {
	
	public ParentClass()
	{
		System.out.println("I am in parent class constructor");
	}
	
	public ParentClass(String name)
	{
		System.out.println("I am in parent class constructor"+name);
	}
	public ParentClass(String name, String address)
	{
		System.out.println("I am in parent class constructor"+name+ " "+address);
	}
	
	

}
